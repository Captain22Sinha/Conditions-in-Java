public class relation_operator {
    public static void main(String[] args) {
        int a = 5 , b = 5 ;
        System.out.println(a==b);
        int c = 5 , d = 9 ; 
        System.out.println(c==d);
        int m = 5 , n = 5 ;
        System.out.println(m!=n);
        int p = 9 , q = 3 ;
        System.out.println(p!=q);
        int x = 74 , y = 26 ; 
        System.out.println(x<y);
        int h = 12 , k = 36 ; 
        System.out.println(h>k);
    }
}
